package a0313;

public class Operator1 {
    public static void main(String[] args) {
        int a=5;
        int b=2;
        int sum=a+b;
        System.out.println("a+b="+sum);
        sum=a-b;
        System.out.println("a-b="+sum);
        sum=a*b;
        System.out.println("a*b="+sum);
        double div=a/b;
        System.out.println("a/b="+div);
        sum=a%b;
        System.out.println("a%b="+sum);
    }
}
