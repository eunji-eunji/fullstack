package a0312;

public class Var4 {
    public static void main(String[] args) {
        // 변수 선언과 초기화를 따로 함.
        int a;  //변수 선언
        a = 1;  //변수 초기화
        System.out.println(a);
        
        int b = 2;  //변수 선언과 초기화를 한 번에
        System.out.println(b);
        
        int c = 3, d = 4; // 여러 변수 한 번에 선언 및 초기화
        System.out.println(c);
        System.out.println(d);


    }
}
