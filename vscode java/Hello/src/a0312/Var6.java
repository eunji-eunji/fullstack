package a0312;

public class Var6 {
    public static void main(String[] args) {
        int a = 100;    //정수. int형
        double b = 10.5;    //실수
        boolean c = true;   //참거짓
        char d = 'A';   //문자 한 개. ''로 써야 함
        String e = "Hello Java";    //문자열. ""로 써야 함
        // 각각의 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
