#include <iostream>
#include <string>
using namespace std;

int main()
{
    int myNum = 5;
    float myFloatNum = 5.99;
    double myDoubleNum = 9.98;
    char letter = 'D';
    bool boolean = true;
    string s="Hello";

    cout << "int: " << myNum << "\n";
    cout << "float: " << myFloatNum << "\n";
    cout << "double: " << myDoubleNum << "\n";
    cout << "char: " << letter << "\n";
    cout << "bool: " << boolean << "\n";
    cout << "string: " << s << "\n";

    return 0;
}
